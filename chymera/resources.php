  <h3>Plasmids Required for CHyMErA Experiments</h3>

  <p>The plasmids required for CHyMErA experiments are available from Addgene:</p>

  <ul class="plist resources-link">
    <li><a href="https://www.addgene.org/73310/">Lenti-Cas9-2A-Blast</a>: lentiviral expression construct for human codon-optimized N-terminal FLAG-tagged Streptococcus pyogenes (Sp)Cas9 nuclease with C-terminal NLS and Blasticidin S resistance.</li>
    <li><a href="https://www.addgene.org/248896/">pLenti-D156R LbCas12a-8xNLS</a> Lentiviral vector expressing human codon-optimized D156R LbCas12a with 8xNLS and Neomycin/G418/Geneticin resistance.</li>
    <li><a href="https://www.addgene.org/209022/">pLenti-opCas12a-6xNLS</a> Lentiviral vector expressing human codon-optimized (As) opCas12a with 6xNLS and Neomycin/G418/Geneticin resistance.</li>
    <li><a href="https://www.addgene.org/248904/">pLCHKOv6</a> Lentiviral backbone for expressing U6 driven hybrid guide (hg)RNAs with BveI cloning sites and puromycin selection marker.</li>
    <li><a href="https://www.addgene.org/155049/">Topo_SpCas9.tracr_LbCas12a.DR</a> TOPO vector for the cloning of the SpCas9 tracrRNA - LbCas12a Direct Repeat (DR) fragment into the pLCHKO hgRNA vector.</li>
    <li><a href="https://www.addgene.org/155050/">Topo_SpCas9.tracr_AsCas12a.DR</a> TOPO vector for the cloning of the SpCas9 tracrRNA - AsCas12a Direct Repeat (DR) fragment into the pLCHKO hgRNA vector.</li>
    <li><a href="https://www.addgene.org/237451/">pLCHKOv4</a> Lentiviral backbone for expressing U6-driven hybrid guide (hg)RNAs for scCHyMErA-Seq experiments.</li>
    <li><a href="https://www.addgene.org/237553/">Topo_SpCas9.tracrRNA-CS1_AsCas12a DRv10</a> TOPO vector for the cloning of the SpCas9 tracrRNA-CS1 - AsCas12a Direct Repeat (DR) v10 fragment into the pLCHKO_v4 hgRNA vector for scCHyMErA-Seq experiments.</li>
  </ul>

